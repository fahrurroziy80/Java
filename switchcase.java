
package kondisi;

public class switchcase {
    public static void main(String[] args) {
        int nilai = 60;
        switch(nilai){
            case 80:
                System.out.println("kamu dapat a");
                break;
            case 70:
                System.out.println("kamu dapat b");
                break;
            case 60:
                System.out.println("kamu dapat c");
                break;
            default:
                System.out.println("kamu tidak d");
        }
    }
}
